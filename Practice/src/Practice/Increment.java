package Practice;

public class Increment {
	
	public static void main(String[] args)
    {

	
	int p =5;
	int q=++p; //pre-increment to increment the value of a variable before assigning it to variable
	
	int a =5;
	int b=a++; ////post-increment to increment the value of a variable before assigning it to
	
	System.out.println(p);
	System.out.println(q);
	System.out.println(a);
	System.out.println(b);
	
}
}
